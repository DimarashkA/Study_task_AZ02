import pandas as pd
import numpy as np

# 1. Создание DataFrame с данными
data = {
    'Имя': ['Алиса', 'Борис', 'Виктор', 'Галина', 'Давид', 'Елена', 'Жанна', 'Игорь', 'Кирилл', 'Людмила'],
    'Математика': [85, 78, 92, 88, 76, 95, 89, 67, 80, 90],
    'Физика': [82, 85, 91, 89, 77, 94, 88, 70, 79, 87],
    'Химия': [78, 76, 85, 87, 75, 90, 82, 69, 77, 86],
    'Биология': [80, 74, 88, 85, 73, 92, 84, 68, 74, 89],
    'История': [75, 70, 80, 78, 72, 85, 81, 66, 73, 83]
}

df = pd.DataFrame(data)

# 2. Вывод первых строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# 3. Вычисление средней оценки по каждому предмету
mean_scores = df.mean(axis=0, numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# 4. Вычисление медианной оценки по каждому предмету
median_scores = df.median(axis=0, numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# 5. Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
print("\nQ1 и Q3 для оценок по математике:")
print(f"Q1 (25-й процентиль): {Q1_math}")
print(f"Q3 (75-й процентиль): {Q3_math}")
print(f"IQR (межквартильный размах): {IQR_math}")

# 6. Вычисление стандартного отклонения
std_deviation = df.std(axis=0, numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)
